# Somme de trois nombres avec l'operateur arithmetique +

# Declaration de trois variables nbr1, nbr2 wt somme de type entier
nbr1 = 5
nbr2 = 4
somme               = nbr1+nbr2
multiplication      = nbr1*nbr2
soustraction        = nbr1-nbr2
