puissance           = nbr1^nbr2

print("La somme de deux nombres              :", nbr1, " + ", nbr2," = ", somme)
print("La multication de deux nombres        :", nbr1, " + ", nbr2," = ", multiplication)
print("La soustraction de deux nombres       :", nbr1, " + ", nbr2," = ", soustraction)
